package kr.co.javaStudy.array;

public class LengthDemo {

	public static void main(String[] args) {
		
		String [] a = new String[5];
		
		System.out.println(a.length);
		
		a[0] = "aaa";
		
		System.out.println(a.length);
		
		a[1] = "bbb";
		
		System.out.println(a.length);
		
		a[2] = "ccc";
		
		System.out.println(a.length);
		
		a[3] = "ccc";
		
		System.out.println(a.length);
		
		a[4] = "ddd";
		
		System.out.println(a.length);
		
	}

}
